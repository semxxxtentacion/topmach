# Глобальные для rate limiting
lock = threading.Lock()
last_request_time = [0.0]
def normalize_domain(netloc):
    netloc = netloc.lower()
    if netloc.startswith('www.'):
        netloc = netloc[4:]
    return netloc
# Retry for Google API
def google_api_call_with_retry(func, max_retries=5, base_delay=1):
    for attempt in range(max_retries):
        try:
            return func()
        except gspread.exceptions.APIError as e:
            if e.response.status_code == 429:
                delay = base_delay * (2 ** attempt) + random.uniform(0, 1)
                print(f"Rate limit hit (attempt {attempt+1}/{max_retries}), waiting {delay:.2f} seconds...")
                time.sleep(delay)
            else:
                raise e
    raise Exception("Max retries exceeded for Google API call.")
# Функция для chunk batch updates
def batch_update_in_chunks(updates, chunk_size=50):
    print("Starting batch update in chunks...")
    for i in range(0, len(updates), chunk_size):
        chunk = updates[i:i+chunk_size]
        google_api_call_with_retry(lambda: worksheet.batch_update(chunk))
        print(f"Batch update chunk completed ({len(chunk)} items)")
        time.sleep(10)  # Увеличенная пауза для write quota
    print("All batch updates completed.")
