# Основная функция
def check_positions(domain_filter=None, query_filter=None):
    print("Fetching all sheet data...")
    rows = google_api_call_with_retry(lambda: worksheet.get_all_values())
    print(f"Fetched {len(rows)} rows from sheet.")
   
    tasks = []
    prev_positions = {}
    for i, row in enumerate(rows[1:], start=2):
        if len(row) < 3: continue
        domain = row[0].strip()
        query = row[1].strip()
        city = row[2].strip()
        if (domain_filter and domain != domain_filter) or (query_filter and query != query_filter):
            continue
        tasks.append((query, domain, city, i))
        prev_pos_str = row[3] if len(row) > 3 else ""
        match = re.search(r'^\[\s*(\d+)\s*\]', prev_pos_str)
        prev_pos = int(match.group(1)) if match else None
        if not match and prev_pos_str:
            print(f"Debug: Regex failed for row {i}, prev_str='{prev_pos_str}'")
        elif prev_pos is None:
            print(f"Debug: No previous position for row {i}")
        prev_positions[i] = prev_pos
   
    if not tasks:
        print("Нет строк для обработки с заданными фильтрами")
        return
   
    # Batch update для E (копируем текущие D в E, но очищаем стрелочки)
    prev_updates = []
    for i in prev_positions:
        prev_pos_str = rows[i-1][3] if len(rows[i-1]) > 3 else ""
        # Очистка стрелочек из строки
        cleaned_str = re.sub(r'\s*(⬆️|⬇️|↕️)$', '', prev_pos_str).strip()
        prev_updates.append({'range': f'E{i}', 'values': [[cleaned_str]]})
    if prev_updates:
        batch_update_in_chunks(prev_updates)
   
    # Сбор позиций
    results = []
    print(f"Starting {len(tasks)} threaded searches...")
    with ThreadPoolExecutor(max_workers=5) as executor:
        futures = {executor.submit(get_search_position, q, d, c): i for q, d, c, i in tasks}
        for future in as_completed(futures):
            row_index = futures[future]
            try:
                position = future.result()
                results.append((row_index, position))
            except Exception as e:
                print(f"Ошибка в потоке для строки {row_index}: {str(e)}")
                results.append((row_index, f"Поток ошибка: {str(e)}"))
    print("All threaded searches completed.")
   
    results.sort(key=lambda x: x[0])
   
    # Форматирование с сравнением и стрелочками
    current_date = time.strftime("%d.%m")
    current_time = time.strftime("%H:%M")
    formatted_results = []
    for row, pos in results:
        arrow = ""
        if isinstance(pos, int):
            prev = prev_positions.get(row)
            if prev is not None:
                if pos < prev:
                    arrow = " ⬆️"
                elif pos > prev:
                    arrow = " ⬇️"
                elif pos == prev:
                    arrow = " ↕️"
                else:
                    print(f"Debug: Position unchanged for row {row} (pos={pos}, prev={prev}), no arrow")
            else:
                print(f"Debug: No prev position for row {row}, skipping arrow")
            formatted_pos = f"[ {pos} ] {current_date} {current_time}{arrow}"
        else:
            formatted_pos = pos
            print(f"Debug: Non-integer position for row {row} ('{pos}'), no arrow")
        formatted_results.append((row, formatted_pos))
   
    # Batch update для D
    updates = [{'range': f'D{row}', 'values': [[pos]]} for row, pos in formatted_results]
    print(f"Подготовлено {len(updates)} обновлений для таблицы")
    batch_update_in_chunks(updates)
    print("Script completed successfully.")

